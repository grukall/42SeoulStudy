char	*ft_strlowcase(char *str)
{
	char	*ch;

	ch = str;
	while (*ch)
	{
		if (*ch >= 'A' && *ch <= 'Z')
		{
			*ch += 32;
		}
		ch++;
	}
	return (str);
}
