int	plus(int one,int two)
{
	return (one + two);
}
int	minus(int one,int two)
{
	return (one - two);
}
int	mok(int one,int two)
{
	return (one / two);
}
int	gop(int one,int two)
{
	return (one * two);
}
int	nanu(int one,int two)
{
	return (one % two);
}
