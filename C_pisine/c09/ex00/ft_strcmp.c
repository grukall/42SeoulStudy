int	ft_strcmp(char *s1, char *s2)
{
	int				i;
	unsigned char	a;
	unsigned char	b;

	i = 0;
	while (s1[i] != '\0' || s2[i] != '\0')
	{
		if (s1[i] != s2[i])
		{
			a = s1[i];
			b = s2[i];
			return ((int)(a - b));
		}
		i++;
	}
	return (0);
}
